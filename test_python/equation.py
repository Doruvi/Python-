class Calculate:
    def __init__(self,num1,num2):
        self.num1 = num1
        self.num2 = num2
    def getAddition(self,num1,num2):
        return self.num1 + self.num2
    
    def getSubtraction(self,num1,num2):
        return self.num1 - self.num2

    def getMultiplication(self,num1,num2):
        return self.num1 * self.num2

    def getDivision(self,num1,num2):
        return self.num1 / self.num2








    

    
